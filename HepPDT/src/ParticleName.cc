//
// getParticleNameMap is the ONLY function allowed to call ParticleNameInit
//
ParticleNameMap const &  getParticleNameMap()
{
  static  ParticleNameMap const &  pmap = ParticleNameInit();
  return pmap;
}  // getParticleNameMap()

bool validParticleName( const int pid )
{
    static  ParticleNameMap const &  pmap = getParticleNameMap();

    ParticleNameMap::const_iterator const cit = pmap.find( pid );
    return ( cit == pmap.end() )
         ? false
	 : true;
}  // validParticleName()

std::string  particleName( const int pid )
{
    static  ParticleNameMap const &  pmap = getParticleNameMap();

    ParticleNameMap::const_iterator const cit = pmap.find( pid );
    return ( cit == pmap.end() )
         ? std::string("not defined")
	 : cit->second;
}  // particleName()

