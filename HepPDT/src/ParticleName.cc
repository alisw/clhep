// list all the defined names
//
void  listHepPDTParticleNames( std::ostream & os  )
{
    os << "     HepPDT Particle List" << std::endl;
    os << std::endl;

    // simple: static  ParticleNameMap const &  pmap = getParticleNameMap();
    // simple: for( ParticleNameMap::const_iterator cit = pmap.begin(), mend = pmap.end(); 
    // simple:                                 cit != mend;
	// simple: 			  ++cit ) {
	// simple: os << "  PDT number: " ;
	// simple: os.width(12);
	// simple: os << cit->first << "  PDT name: " << cit->second << std::endl;
    // simple: }
    int id, i, j, q1, q2, q3, l, m, n;
    // special cases
    for( id=1; id<101; ++id) {
        writeParticleNameLine(  id, os );
        writeParticleNameLine( -id, os );
    }
    for( i=11; i<1000; ++i) {
        id = i*10;
        writeParticleNameLine(  id, os );
        writeParticleNameLine( -id, os );
    }
    // SUSY, technicolor, etc.
    for( n=1; n<6; ++n) {
        for( q2=0; q2<10; ++q2) {
	    for( q1=0; q1<10; ++q1) {
	        for( j=0; j<10; ++j) {
		    for( m=0; m<10; ++m) {
		        for( l=0; l<7; ++l) {
			    id = 1000000*n+100000*m+10000*l+100*q2+10*q1+j;
        		    writeParticleNameLine(  id, os );
        		    writeParticleNameLine( -id, os );
			}
		    }
		}
	    }
	}
    }
    // miscellaneous generator particles
    for( i=1; i<100; ++i) {
	id = 9900000+i;
        writeParticleNameLine(  id, os );
        writeParticleNameLine( -id, os );
    }
    for( l=1; l<9; ++l) {
        for( q3=0; q3<10; ++q3) {
            for( q2=0; q2<10; ++q2) {
		for( q1=0; q1<10; ++q1) {
	            for( j=0; j<10; ++j) {
			id = 9900000+10000*l+1000*q3+100*q2+10*q1+j;
        		writeParticleNameLine(  id, os );
        		writeParticleNameLine( -id, os );
		    }
		}
	    }
	}
    }
    // diquark
    for( i=11; i<100; ++i) {
        for( j=0; j<10; ++j) {
            id = 100*i+j;
            writeParticleNameLine(  id, os );
            writeParticleNameLine( -id, os );
	}
    }
    // mesons
    for( q2=1; q2<10; ++q2) {
	for( q1=1; q1<10; ++q1) {
	    for( j=1; j<10; ++j) {
		for( m=0; m<9; ++m) {
		    for( l=0; l<10; ++l) {
			id = 100000*m+10000*l+100*q2+10*q1+j;
        		writeParticleNameLine(  id, os );
        		writeParticleNameLine( -id, os );
			id = 9000000+100000*m+10000*l+100*q2+10*q1+j;
        		writeParticleNameLine(  id, os );
        		writeParticleNameLine( -id, os );
		    }
		}
	    }
	}
    }
    // baryons
    for( q3=1; q3<10; ++q3) {
	for( q2=1; q2<10; ++q2) {
	    for( q1=1; q1<10; ++q1) {
		for( j=1; j<10; ++j) {
		    id = 1000*q3+100*q2+10*q1+j;
        	    writeParticleNameLine(  id, os );
        	    writeParticleNameLine( -id, os );
		}
	    }
	}
    }
    // ions
    for( i=1; i<3; ++i) {
	for( m=1; m<5; ++m) {
            for( j=1; j<3; ++j) {
		id = 1000000000+1000000*m+1000*i+j;
        	writeParticleNameLine(  id, os );
        	writeParticleNameLine( -id, os );
	    }
	}
    }
    return;
}  // listHepPDTParticleNames()

